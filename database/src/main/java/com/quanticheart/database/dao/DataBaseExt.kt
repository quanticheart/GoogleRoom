package com.quanticheart.database.dao

import android.content.Context
import com.quanticheart.database.entity.UserData
import org.jetbrains.anko.doAsync

fun Context.userdata(userdata: (UserData) -> Unit) {
    val r = DataBaseRepository(this)
    doAsync {
        userdata(r.get())
    }
}

fun Context.updateUserdata(userData: UserData) {
    val r = DataBaseRepository(this)
    doAsync {
        r.update(userData)
    }
}

fun Context.clearUserData() {
    val r = DataBaseRepository(this)
    doAsync {
        r.delete(r.get())
    }
}

fun Context.insertUserData(userData: UserData) {
    val r = DataBaseRepository(this)
    doAsync {
        r.insert(userData)
    }
}
