package com.quanticheart.database.dao.base.daoCreate

import android.content.Context
import android.telecom.Call
import android.util.Log
import androidx.room.Room
import androidx.room.RoomDatabase
import androidx.sqlite.db.SupportSQLiteDatabase
import com.quanticheart.database.BuildConfig
import com.quanticheart.database.dao.DataBase

internal open class BaseRepository(context: Context) {
    private val dbName = "app_database"
    protected val db: DataBase

    init {
        db = Room.databaseBuilder(context, DataBase::class.java, dbName)
            .addCallback(object : RoomDatabase.Callback() {
                override fun onCreate(db: SupportSQLiteDatabase) {
                    super.onCreate(db)
                    alert("New DataBase Create!!")
                }

                override fun onOpen(db: SupportSQLiteDatabase) {
                    super.onOpen(db)
                    alert("App database opened!")
                }
            }).build()
    }

    private fun alert(msg: String) {
        if (BuildConfig.DEBUG) Log.w("DataBase Alert", msg)
    }
}