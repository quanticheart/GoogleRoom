package com.quanticheart.database.dao

import android.content.Context
import com.quanticheart.database.dao.base.daoCreate.BaseRepository
import com.quanticheart.database.entity.UserData

@Suppress("PrivatePropertyName", "unused", "MemberVisibilityCanBePrivate")
internal class DataBaseRepository(context: Context) : BaseRepository(context) {

    /**
     * Inserts
     */
    fun insert(user: UserData) {
        db.daoUserData().insert(user)
    }

    fun insertList(user: List<UserData>) {
        db.daoUserData().insert(user)
    }

    /**
     * Update
     */
    fun update(user: UserData) {
        db.daoUserData().update(user)
    }

    /**
     * delete
     */
    fun delete(user: UserData) {
        db.daoUserData().delete(user)
    }

    /**
     * Get Data
     */
    fun get(): UserData {
        return db.daoUserData().getUser()
    }
}
